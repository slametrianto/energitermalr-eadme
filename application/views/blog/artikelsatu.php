                <p align="justify"> Sangat terintegrasi dan menghemat ruang: satu kabinet standar menyediakan kondisi operasi yang stabil untuk semua perangkat IT, hanya koneksi kabinet dan pemasangan AC yang dibutuhkan di sisi user. Sistem kabinet ini hanya menempati 0,7 meter persegi.</p>

                <h2 class="section-heading">UPS Double Conversion Online</h2>

                <p align="justify">Tipe ini merupakan yang paling lazim untuk UPS dengan daya lebih dari 10kVA. Tipe ini memiliki kesamaan dengan tipe Standby. Hanya saja tipe ini memiliki sumber tenaga utama yang terletak pada Inverter, bukan pada sumber listrik AC. Dapat dikatakan tipe ini mendekati gambaran ideal dari sebuah UPS, sayangnya tipe ini menghasilkan panas yang cukup tinggi.</p>

