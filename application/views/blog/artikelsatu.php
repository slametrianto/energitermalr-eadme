                <h2 class="section-heading">Vertiv SmartCabinet : Ruang Server Mini Dalam Satu Kabinet</h2>
                <p align="justify"> Ingin membangun ruang server tapi bingung karena workspace yang tersedia tidak cukup luas? Atau, khawatir dengan waktu yang dibutuhkan untuk proses develop hingga pembangunan ruang server tersebut, serta kebisingan yang akan dihasilkan?</p>

                <p align="justify">Jangan lupakan masalah cooling management yang dibutuhkan agar suhu di dalam ruang server tetap terjaga dengan baik, serta kebutuhan UPS yang wajib ada untuk menopang distribusi back up daya yang dibutuhkan oleh unit server Anda.</p>

                <p align="justify">Memperkenalkan, Vertiv SmartCabinet (MSC-A5S652BMNNXX), intelligent and integrated containment for IT Infrastructure.</p>

                <p align="justify">Dengan solusi “ruang server mini dalam 1 kabinet” dari Vertiv (sebelumnya dikenal dengan nama Emerson Liebert), segala kerepotan di atas akan langsung sirna. Karena solusi SmartCabinet (MSC-A5S652BMNNXX) dari Vertiv dapat menjawab segala kebutuhan yang Anda butuhkan untuk membangun ruang server milik Anda.</p>

                <p align="justify">Dengan SmartCabinet (MSC-A5S652BMNNXX) yang bahkan gampang untuk dirakit dan dipindahkan, Anda dapat memiliki ruang server mini dalam 1 kabinet yang telah dilengkapi dengan sistem cooling (thermal management & emergency fans), unit UPS & batteries, sentralisasi sistem monitoring, panel LCD display yang intuitif, built-in Power Management Unit, serta lighting system untuk menerangi bagian interior dalam kabinet.</p>

                <h2 class="section-heading">FITUR STANDAR SMARTCABINET</h2>

                <p align="justify">Perlindungan dari debu dan pengurangan kebisingan, efisiensi tinggi dan penghematan energi: sistem operasi tertutup dan siklus internalnya memastikan temperatur dan kelembaban di dalam kabinet, untuk memperpanjang masa pakai perangkat IT. UPS yang sangat efisien dan pendingin AC Presisi dengan teknologi cold/hot aisle untuk tujuan meningkatkan efek pendinginan. Fan/Kipas ini didesain dengan kebisingian silent/diam, yang bisa digunakan di area perkantoran.</p>

                <p align="justify"> Intelligent monitoring: fungsi intelligent control seperti pemantauan lingkungan terpadu, pemantauan perangkat, hubungan alarm dan sebagainya menyediakan platform pemantauan terpusat untuk pengelolaan ruang komputer.</p>

