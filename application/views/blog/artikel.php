                <p align="justify">Selain mengetahui beberapa fungsi danm kegunaan dari UPS di atas, Anda juga perlu tahu beberapa macam jenis dan tipe UPS yang ada di pasaran agar bisa disesuaikan dengan kebutuhan Anda sebagai pengguna. Secara umum berikut ini beberapa merek UPS terkenal yang bagus yang sering digunakan dan juga ada di pasaran seperti APC, CyberPower Delta, Emerson, IBM, ICA, Matsumoto, Micropack, Montero, Prolink, Yuasa dan yang lainya dengan macam jenis dan tipe UPS seperti berikut:</p>

                <p align="justify">merupakan jenis UPS rumahan yang paling banyak digunakan. Anda juga mungkin memilikinya. Harganya pun lebih terjangkau, di bawah 500ribu rupiah untuk merek standar. Untuk penggunaan alat-alat sederhana, jenis UPS inilah yang digunakan. Memiliki tingkat efisiensi yang tinggi, akan tetapi juga memiliki beberapa kekurangan. Diantaranya adalah baterai tetap terpakai meskipun dalam kondisi mati serta tidak bisa digunakan untuk pemakaian di atas 2kva. Jadi memang untuk ukuran rumahan saja.</p>

