                <p align="justify">UPS ini merupakan bentuk teknologi Konversi Ganda (Double Conversion) yang terah diperbaharui dan tersedia dengan daya 5kVA hingga 1.6MW. Memiliki kemiripan dengan tipe Double Conversion, tipe ini menggunakan Inverter untuk selalu memasok voltase. Saat pasokan tenaga terputus, tipe ini melakukan hal yang sama dengan tipe Double Conversion.</p>

                <p align="justify">Pastikan UPS dilengkapi dengan AVR(stabilizer) dan Fuse (anti petir), sehingga tegangan listrik yang turun atau naik dan tidak stabil dapat dicegah sehingga kerusakan pada perangkat komputer dapat diminimalisir. Dan untuk Kalangan Pengguna rumahan, Jika anda memiliki Komputer, Printer, Scanner maka gunakan UPS yang tidak terlalu besar kapasitasnya karena yang paling penting perangkat komputer bisa bertahan beberapa menit untuk mematikannya secara normal sehingga mencegah kerusakan pada Komputer dan perangkat lainnya.</p>

