                <p align="justify"> Pengertian lengkap, macam tipe jenis dan kegunaan serta cara kerja UPS pada Komputer. Apa itu UPS? Sebagian Anda mungkin tak asing lagi dengan perangkat elektronik yang satu ini. jika kita memiliki perangkat elektronik di rumah, biasanya kita juga memiliki perangkat ups sederhana yang digunakan. Nah, untuk memudahkan Anda dalam memahaminya, sebaiknya Anda tau lebih dulu apa definisi sederhana dari UPS tersebut.</p>

                <p align="justify">UPS merupakan singkatan atau kepenjanagn dari Uninterruptible Power Supply sebuah alat tambahan pada rangkaian komputer dan biasanya dipasang pada peralatan listrik dan elektronik di rumah. UPS yang sederhana biasanya akan dipasang orang di peralatan seperti TV, kulkas, komputer dan sebagainya.</p>

                <p align="justify">Pentingnya menggunakan UPS pada komputer desktop, membuat kita harus mengetahui juga, apa sih fungsi UPS yang sebenarnya? Jangan sampai Anda disarankan oleh penjual untuk membeli UPS, tapi Anda tidak mengetahui gunanya untuk apa. Lalu apa sebenarnya fungsi dari UPS tersebut? Secara umum sebenarnya alat ini bertujuan untuk menstabilkan arus listrik yang diterima oleh peralatan elektronik yang dipasangkan padanya.</p>

                <p align="justify">Misalnya komputer atau kulkas, dengan adanya peralatan UPS tersebut akan membuat peralatan tersebut menjadi lebih stabil asupan arusnya sehingga peralatan lebih awet dan tidak cepat rusak. Beberapa fungsi lain diantaranya adalah sebagai berikut:</p>

                <p align="justify">Melindungi peralatan elektronik dari kondisi arus yang tidak stabil, mati mendadak dan sebagainya. Di daerah yang memiliki kebiasaan pemadaman listrik sebaiknya peralatan elektronik di rumah wajib menggunakan perangkat UPS supaya lebih tahan lama usia penggunaannya.
                    Bagi mereka yang menggunakan sumber arus kurang stabil seperti mesin genset atau diesel, sebaiknya menggunakan peralatan UPS. Karena akan berbahaya jika arus tiba-tiba naik atau tiba-tiba turun tidak terkontrol. Anda mungkin pernah melihat beberapa lampu yang terbakar pada penggunaan peralatan elektronik yang tidak menggunakan UPS atau yang juga sering disebut dengan alat penstabil arus, stabilizer.
                    UPS mampu memberikan arus cadangan apabila tiba-tiba sumber arus mati. Maka peralatan elektronik bisa dimatikan dan tidak mati secara tiba-tiba karena akan sangat membahayakan kondisi peralatan elektronik.
                    Terutama bagi para pengguna komputer dan server, alat UPS sangat dibutuhkan. Fungsi back up data dari peralatan UPS akan membantu para pengguna komputer dan server tidak kehilangan data yang dibutuhkan.</p>

